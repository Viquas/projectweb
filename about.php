      $('a.next').css("display", "none");
    }
  }, 500);


 });

 $(document).on('click', 'a.controls', function(){
   //this is where we add our logic
   var index = $(this).attr('href');
   var src = $('ul.row li:nth-child('+ index +') img').attr('src');
   var name = $('ul.row li:nth-child('+ index +') img').attr('alt');
   var designation = $('ul.row li:nth-child('+ index +') .text-content').text();
   var description = $('ul.row li:nth-child('+ index +') .text-desc').text();
    $('.modal-body img').attr('src', src);
    $('.team-name').text(name);
    $('.team-designation').text(designation);
    $('.team-description').text(description);
    var newPrevIndex = parseInt(index) - 1;
    var newNextIndex = parseInt(newPrevIndex) + 2;

    if($(this).hasClass('previous')){
      $(this).attr('href', newPrevIndex);
      $('a.next').attr('href', newNextIndex);
    }else{
      $(this).attr('href', newNextIndex);
      $('a.previous').attr('href', newPrevIndex);
    }
    var total = $('ul.row li').length + 1;
    //hide next button
    if(total === newNextIndex){
      $('a.next').hide();
    }else{
      $('a.next').show()
    }
    //hide previous button
    if(newPrevIndex === 0){
      $('a.previous').hide();
    }else{
      $('a.previous').show()
    }

    $('#myModal').on('shown.bs.modal', function(){
      $('#myModal .modal-body').html(html);
      //this will hide or show the right links:
      $('a.controls').trigger('click');
    })
    return false;
  });


});



</script>
