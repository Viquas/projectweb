  img += '<div class="team-content"><p class="team-description">'+ c +'</p></div>';
  img += '</div></div><div class="col-xs-1 no-padding"> <a class="controls next" href="'+ (index+2) + '" style="margin-top:125px;"><i class="fa fa-icon fa-arrow-right"></i></a></div> ';
